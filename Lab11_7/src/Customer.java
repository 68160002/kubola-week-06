public class Customer {
    int id;
    Fullname name;
    String tel;
    public int getId (){
        return id;
    }
    public void setId ( int i){
        this.id = i;
    }
    public Fullname getName (){
        return name;
    }
    public void setName (Fullname name){
        this.name = name;
    }
    public String getTel(){
        return tel;
    }
    public void setTel( String tel){
        this.tel = tel;
    }
}
